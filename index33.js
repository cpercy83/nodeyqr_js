let userCount = 100;

userCount += 5; // increment by 5
console.log(`User Count: ${userCount}`);

userCount -= 3; // decrement by 3
console.log(`User Count: ${userCount}`);

userCount *= 2; // double
console.log(`User Count: ${userCount}`);

userCount /= 2; // halve
console.log(`User Count: ${userCount}`);

let menCount, womenCount; // new variables
menCount = womenCount = userCount/2; // each variable as half
console.log(`User Count: ${userCount}, Men: ${menCount}, Women: ${womenCount}`);

