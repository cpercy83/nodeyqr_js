let weekdayNumber = 14;
let message = null;

switch (weekdayNumber) {
case 1:
        message = "Happy Monday!";
break;
case 2:
    message = "Happy Tuesday!";
break;
case 3:
    message = "Happy Hump Day!";
break;
case 4:
    message = "Happy Turdsday!";
break;
case 5:
    message = "Happy Friyay!";
break;
case 6:
    message = "Enjoy Your Saturday!";
break;
case 7:
    message = "Enjoy Your Sunday!";
break;
default: message = "Come Back When You Know the Days of the Week!";
}

console.log(`${message}`);