let firstName = "Clintron";
let lastName = "PurplePeopleEater";
let nameLength = firstName.length;

console.log(
  `My first name is ${firstName} and my name is ${nameLength} characters long\.`
);

let nameLengthLast = lastName.length;

console.log(
  `My last name is ${lastName} and my name is ${nameLengthLast} characters long\.`
);

let nameDiff = nameLengthLast - nameLength;

console.log(
  `My last name is ${nameDiff} characters longer than my first name\.`
);

let nameSum = nameLength + nameLengthLast;

console.log(
  `Combined, my first and last names are ${nameSum} characters long\.`
);
